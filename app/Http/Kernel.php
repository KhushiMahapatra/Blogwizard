protected function schedule(Schedule $schedule)
{
    $schedule->call(function () {
        $posts = Post::where('status', 'scheduled')
                     ->where('published_at', '<=', now())
                     ->get();

        foreach ($posts as $post) {
            $post->status = 'published';
            $post->save();
        }
    })->everyMinute();
}
